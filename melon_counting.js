function count_melons(melonList) {
  var melonCounts = {};

  for (var i = 0; i < melonList.length; i++) {
    if (melonList[i] in melonCounts) {
      melonCounts[melonList[i]] += 1;
    } else {
      melonCounts[melonList[i]] = 1;
    }
  }
  return melonCounts;
}